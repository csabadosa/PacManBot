    public Menu(Main main) {
        try {
            //create the font to use. Specify the size!
            customFont = Font.createFont(Font.TRUETYPE_FONT, new File("src/font/pac_font.ttf")).deriveFont(12f);
            GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
            //register the font
            ge.registerFont(customFont);
        } catch (IOException e) {
            e.printStackTrace();
        } catch(FontFormatException e) {
            e.printStackTrace();
        }
        b1 = new JButton("Start");
        b2 = new JButton("Score");
        b3 = new JButton("Exit");
        b1.setFont(customFont);
        b1.setForeground(Color.CYAN);
        b1.setOpaque(false);
        b1.setContentAreaFilled(false);
        b1.setBorderPainted(false);

        b1.addActionListener(e->{
            try {
                main.showGame();
            } catch (InterruptedException ex) {
                ex.printStackTrace();
            }
            ;
        });
        b2.setFont(customFont);
        b2.setForeground(Color.pink);
        b2.setOpaque(false);
        b2.setContentAreaFilled(false);
        b2.setBorderPainted(false);

        b3.setFont(customFont);
        b3.setForeground(Color.red);
        b3.setOpaque(false);
        b3.setContentAreaFilled(false);
        b3.setBorderPainted(false);
        b3.addActionListener(e->{
                main.exitApp();
        });



        backg = new ImageIcon("src/images/menu.png");
        this.setLayout(null);
        b1.setBounds(237, 250, 100,30);
        b2.setBounds(237, 300, 100,30);
        b3.setBounds(237, 350, 100,30);
        add(b1);
        add(b2);
        add(b3);
        setBackground(Color.black);

    }

