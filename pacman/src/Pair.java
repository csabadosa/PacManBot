public class Pair {
    private int sorsz;
    private double ert;

    public Pair(int sorsz, double ert) {
        this.sorsz = sorsz;
        this.ert = ert;
    }

    public int getSorsz() {
        return sorsz;
    }

    public void setSorsz(int sorsz) {
        this.sorsz = sorsz;
    }

    public double getErt() {
        return ert;
    }

    public void setErt(double ert) {
        this.ert = ert;
    }
}
